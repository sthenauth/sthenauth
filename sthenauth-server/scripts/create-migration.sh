#!/bin/sh

################################################################################
set -e

################################################################################
dir=$(realpath "$(dirname "$0")/../schema")

################################################################################
if [ $# -ne 1 ]; then
  >&2 echo "ERROR: give the name for a new SQL file"
  exit 1
fi

################################################################################
name=$(date +%Y%m%dT%H%M%S)-"$1".sql
touch "$dir/$name"
echo "$name"
